#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
    while (1)
    {
	    if (*s == c)
		    return (s);
	    else if (c == '\0')
		    return (NULL);
	    else
		    s++;
    }
    return (NULL);
}
